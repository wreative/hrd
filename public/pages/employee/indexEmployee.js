"use strict";

var table = $("#table").DataTable({
    pageLength: 10,
    processing: true,
    serverSide: true,
    responsive: true,
    lengthMenu: [
        [10, 25, 50, -1],
        [10, 25, 50, "Semua"]
    ],
    ajax: {
        url: "/reports",
        type: "GET",
        data: function(d) {
            d.filter_period = $("#filter_period").val();
        }
    },
    dom: '<"html5buttons">lBrtip',
    columns: [
        { data: "DT_RowIndex", orderable: false, searchable: false },
        { data: "name" },
        { data: "tgl" },
        { data: "status" },
        { data: "category" },
        { data: "action", orderable: false, searchable: true }
    ],
    buttons: [
        {
            extend: "print",
            text: "Print Semua",
            exportOptions: {
                modifier: {
                    selected: null
                },
                columns: ":visible"
            },
            messageTop: "Dokumen dikeluarkan tanggal " + moment().format("L"),
            // footer: true,
            header: true
        },
        {
            extend: "csv"
        },
        {
            extend: "print",
            text: "Print Yang Dipilih",
            exportOptions: {
                columns: ":visible"
            }
        },
        {
            extend: "excelHtml5",
            exportOptions: {
                columns: ":visible"
            }
        },
        {
            extend: "pdfHtml5",
            exportOptions: {
                columns: [0, 1, 2, 5]
            }
        },
        {
            extend: "colvis",
            postfixButtons: ["colvisRestore"],
            text: "Sembunyikan Kolom"
        }
    ]
});

$(".filter_name").on("keyup", function() {
    table.search($(this).val()).draw();
});
$(".filter_status").on("change", function() {
    table
        .column($(this).data("column"))
        .search($(this).val())
        .draw();
});
$("#filter_period").on("change", function() {
    table.draw();
});
